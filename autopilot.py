class auto_pilot_interface:

    def __init__(self, iface, dialog, plugin_dir):
        """
        Constructor.

        :param iface: An interface instance that will be passed to this class
            which provides the hook by which you can manipulate the QGIS
            application at run time.
        :type iface: QgsInterface
        :param dialog: The QuODK UI dialog
        :param plugin_dir: Plugin directory path from main plugin
        """
        # Save reference to the QGIS interface
        self.iface = iface
        self.dlg = dialog
        self.plugin_dir = plugin_dir
        # Declare instance attributes
        self.session_token = ''
        self.data_grabber = None  # Will be initialized after authentication
        # Default values
        self.odkURL = ''
        self.odkUser = ''
        self.odkPass = ''
        self.pid = 0
        self.xmlFormId = ''
        self.FormName = ''
        self.projection = 'EPSG:4326'
        self.headFont = QFont()
        self.LayerName = ''
        self.auto_entitystatus = ["New", "Updated"]
