        if self.dlg.Elist_label.currentIndex() > 0:
            self.reset_msg()

            if self.dlg.selected_features.isChecked():
                get_features = QgsVectorLayerUtils.getValues(
                    self.Elist_layer,
                    self.dlg.Elist_label.currentText(),
                    selectedOnly=True
                )
            else:
                get_features = QgsVectorLayerUtils.getValues(
                    self.Elist_layer,
                    self.dlg.Elist_label.currentText()
                )

            check_key = get_features[0]
            unique = set(get_features[0])
            nonull = list(filter(None, check_key))

            if len(check_key) != len(nonull):
                self.set_msg(self.tr_label_includes_null, "color: #ffffff; background: red", 'entities')
                self.dlg.Elist_label.setStyleSheet("background: red")
