    def set_geometry(self):
        """Set up geometry for selected submissions/entities with optional filtering."""
        self.reset_msg()

        # Get appropriate dataframe
        if self.keyLevel == "KEY":
            df_setgeom = self.df_submissions
        elif self.keyLevel == "ENTITY_KEY":
            df_setgeom = self.df_entities
        elif self.keyLevel == "PARENT_KEY":
            df_setgeom = self.df_repeat
        else:
            return

        # Apply filters if set - use manual filter state
        filter_state = self.manual_filter
        if self.keyLevel in ("KEY", "ENTITY_KEY"):
            if filter_state['valueFilter'] and filter_state['attributeFilter']:
                if filter_state['filterExclude'] == "True":
                    df_setgeom = df_setgeom[df_setgeom[filter_state['attributeFilter']] != filter_state['valueFilter']]
                else:
                    df_setgeom = df_setgeom[df_setgeom[filter_state['attributeFilter']] == filter_state['valueFilter']]

        # Handle geometry column filtering
        if hasattr(self.dlg, 'odk_geometry') and self.dlg.odk_geometry.currentIndex() > 1:
            geomType = self.dlg.odk_geometry.currentText()
            if self.keyLevel != "ENTITY_KEY":
                if "calculated_geometry" in geomType:
                    geomCol = geomType
                elif geomType == self.tr_table_only:
                    geomCol = ""
                else:
                    geomCol = str(str(geomType).split("--->")[0]) + '-coordinates'
            else:
                geomCol = geomType

            # Filter to only rows with geometry (unless table-only or ignore_nogeom checked)
            if geomCol and hasattr(self.dlg, 'ignore_nogeom'):
                if not self.dlg.ignore_nogeom.isChecked():
                    df_setgeom = df_setgeom.dropna(subset=[geomCol])

        #Update the table display and message box
        self.updateTable(df_setgeom, 'manual')

        # Manage button states based on geometry selection
        if hasattr(self.dlg, 'odk_geometry') and self.dlg.odk_geometry.currentIndex() > 0:
            self.dlg.load_to_canvas.setEnabled(True)
            self.dlg.save_csv.setEnabled(True)
            if self.keyLevel != "ENTITY_KEY":
                self.dlg.Attachments.setEnabled(True)
            else:
                self.dlg.Attachments.setEnabled(False)
        else:
            self.dlg.load_to_canvas.setEnabled(False)
            self.dlg.save_csv.setEnabled(False)
            self.dlg.Attachments.setEnabled(False)

    def get_filter_state(self, trigger):
        """
        Get the appropriate filter state dictionary for the given trigger context.

        Args:
            trigger (str): Context identifier ('manual', 'config', 'entity_config', or 'auto')

        Returns:
            dict: Filter state dictionary with keys: attributeFilter, valueFilter, filterExclude
        """
        if trigger == 'manual':
            return self.manual_filter
        elif trigger == 'config':
            return self.config_filter
        elif trigger == 'auto':
            # Auto-pilot uses loaded config values, returns manual for result storage
            return self.manual_filter
        elif trigger == 'entity_config':
            return self.entity_filter
        else:
            # Fallback for legacy code still using old pattern
            return self.manual_filter

