    def onDateChanged(self, trigger):
        """Handle date range changes, converting to UTC for API."""
        if trigger == 'init':
            if hasattr(self.dlg, 'DateEnd'):
                self.dlg.DateEnd.setDate(self.DateEnd)
            if hasattr(self.dlg, 'DateStart'):
                self.dlg.DateStart.setDate(self.DateStart)
                self.DateStart = quodk_lib.timeaware(self.dlg.DateStart.dateTime())

        elif trigger == 'manual':
            if hasattr(self.dlg, 'DateStart'):
                self.dlg.DateStart.setDate(self.dlg.DateStart.date())
                self.DateStart = quodk_lib.timeaware(self.dlg.DateStart.dateTime())

        if hasattr(self.dlg, 'DateEnd'):
            self.dlg.DateEnd.setDate(self.dlg.DateEnd.date())
            self.DateEnd = (quodk_lib.timeaware(self.dlg.DateEnd.dateTime()) +
                            timedelta(hours=23, minutes=59, seconds=59.99))

        if self.DateStart > self.DateEnd:
            self.set_msg(self.tr_date_error, "color: black; background: none", trigger)
            return
        else:
            # Convert to API-readable string format
            self.strSdate = str(self.DateStart).replace(" ", "T") + "Z"
            self.strEdate = str(self.DateEnd).replace(" ", "T") + "Z"

        # Trigger submission list refresh if form/repeat selected or entities in manual mode
        if trigger == 'manual':
            self.DateUpdate()
        else:
            return
