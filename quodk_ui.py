    def connectODK(self, trigger):
        """Connect to ODK Central and initialize session."""
        self.reset_msg()

        if trigger == 'manual':
            odk_url = self.dlg.odk_url.text()
            self.dlg.ODK_connect.setStyleSheet("background: orange")
            self.dlg.projectID.clear()
            self.dlg.formID.clear()
            self.dlg.repeatGroup.clear()
            self.dlg.attributeFilter.clear()
            if odk_url.endswith("/"):
                odk_url = odk_url[:-1]
                self.dlg.odk_url.setText(odk_url)

            odk_username = self.dlg.odk_username.text()
            odk_pass = self.dlg.odk_pass.text()

            # If using QGIS Auth, retrieve current credentials from Auth Manager
            use_qgis_auth = self.dlg.use_qgis_auth.isChecked() if hasattr(self.dlg, 'use_qgis_auth') else self.use_qgis_auth
            authcfg = self.dlg.authcfg.text() if hasattr(self.dlg, 'authcfg') else self.authcfg
            if use_qgis_auth and authcfg:
                success, user, password = self.get_credentials_from_authcfg(authcfg)
                if success:
                    odk_username = user
                    odk_pass = password
            #print(odk_url,odk_username,odk_pass)
        elif trigger == 'auto':
            odk_url = self.odkURL
            odk_username = self.odkUser
            odk_pass = self.odkPass
        else:
            return

        # Try to get session token using library
        try:
            self.set_msg(self.tr_server_connecting, "background: none", trigger)
            session_manager = quodk_lib.SessionToken(odk_url, odk_username, odk_pass)
            self.session_token = session_manager.get_token()
            #print('getting token', self.session_token)
            # Initialize data grabber with token
            self.data_grabber = quodk_lib.CentralDataGrab(self.session_token, odk_url)

            # Update UI on success
            self.dlg.projectID.addItem(self.tr_projectID)
            self.dlg.ODK_connect.setStyleSheet("background: DarkSeaGreen")
            self.dlg.ODK_connect_test.setStyleSheet("background: DarkSeaGreen")
            self.projects_list()

        except ValueError:
            # Authentication failed
            self.dlg.connection_msg.setText(self.tr_check_server)
            self.dlg.msg.setStyleSheet("background: red")
            self.dlg.ODK_connect.setStyleSheet("background: red")
            #self.dlg.quodk_tab.setCurrentIndex(3)
            self.session_token = ''

        except ConnectionError:
            # Connection failed
            self.dlg.connection_msg.setText('Please check this a valid URL')
            self.dlg.ODK_connect.setStyleSheet("background: red")
            #self.dlg.quodk_tab.setCurrentIndex(3)
            self.session_token = ''

