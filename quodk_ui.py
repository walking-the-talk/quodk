    def precheck_csv(self):
        """Generate entity dataframe with geometry conversion for CSV export."""
        if not self.dlg.Elist_name.text():
            self.set_msg(self.tr_entity_name_required, "background: none", 'entities')
            self.dlg.Elist_name.setStyleSheet("background: red")
            return

        eid = ""
        if self.dlg.Elist_label.currentIndex() <= 0:
            self.set_msg(self.tr_label_not_unique, "background: none", 'entities')
            self.dlg.Elist_label.setStyleSheet("background: red")
            return

        self.entity_match_central()

        if not self.dlg.Elist_properties.currentText():
            self.set_msg(self.tr_no_entity_properties, "background: none", 'entities')
            return

        self.reset_msg()
        label = str(self.dlg.Elist_label.currentText())

        if self.dlg.QGIS_KEY.currentIndex() > 0:
            eid = str(self.dlg.QGIS_KEY.currentText())

        geomtype = QgsWkbTypes.displayString(self.Elist_layer.wkbType())
        coordDimensions = QgsWkbTypes.coordDimensions(self.Elist_layer.wkbType())

        if geomtype == "Unknown":
            self.set_msg(self.tr_unknown_geometry, "background: none", 'entities')
            return

        if QgsWkbTypes.isMultiType(self.Elist_layer.wkbType()):
            self.set_msg(self.tr_multipart, "background: none", 'entities')
            self.dlg.Elist_label.clear()
            self.dlg.QGIS_KEY.clear()
            return

        cols = ([f.name() for f in self.Elist_layer.fields()] +
                ['geometry'] + ['__id'] + ['base_version'])
        row_list = []
        nogeom_row = 0

        if self.dlg.selected_features.isChecked():
            get_layer_features = self.Elist_layer.selectedFeatures()
        else:
            get_layer_features = self.Elist_layer.getFeatures()

        for f in get_layer_features:
            # Convert QGIS geometry to ODK format
            geometry = ""
            geom = f.geometry()

            # Transform from layer CRS to WGS84
            destCrs = QgsCoordinateReferenceSystem("EPSG:4326")
            sourceCrs = QgsCoordinateReferenceSystem(self.Elist_layer.crs())

            tr = None
            if self.Elist_layer.crs().authid() != "EPSG:4326":
                tr = QgsCoordinateTransform(sourceCrs, destCrs, QgsProject.instance())

            if geom:
                if tr:
                    geom.transform(tr)
                coords = geom.constGet().vertices()

                for vertex in coords:
                    if coordDimensions == 2:  # XY - 2D
                        zm = ' 0 0'
                    elif coordDimensions == 3:  # XYZ - 3D
                        zm = ' ' + str(vertex.z()) + ' 0'
                    elif coordDimensions == 4:  # XYZM - 4D
                        zm = ' ' + str(vertex.z()) + ' ' + str(vertex.m())

                    if geometry == "":
                        geometry = str(vertex.y()) + ' ' + str(vertex.x()) + zm
                    else:
                        geometry += '; ' + str(vertex.y()) + ' ' + str(vertex.x()) + zm
            else:
                nogeom_row += 1

            if eid:
                ENTITY_KEY = uuid.UUID(f[eid])
            else:
                ENTITY_KEY = uuid.uuid4()

            try:
                if f['__system.version']:
                    ENTITY_VERSION = f['__system.version']
            except (KeyError, TypeError, IndexError):
                ENTITY_VERSION = 0

            row_list.append(dict(zip(cols, (f.attributes() + [geometry] +
                                            [ENTITY_KEY] + [ENTITY_VERSION]))))

        self.df_create_entityList = pd.DataFrame(row_list, columns=cols)

        if nogeom_row:
            self.set_msg(str(nogeom_row) + ' / ' + str(len(row_list)) + self.tr_nogeom_row,
                "background: none", 'entities')

        # Select columns
        if self.useEntity is True:
            if 'label' in self.existing_list_props:
                self.existing_list_props.remove('label')
            if 'geometry' in self.existing_list_props:
                self.existing_list_props.remove('geometry')
            selected_columns = (['__id'] + [label] + ['geometry'] +
                               self.existing_list_props + ['base_version'])
        else:
            selected_columns = (['__id'] + [label] + ['geometry'] +
                               self.dlg.Elist_properties.checkedItems() + ['base_version'])

        exclude_columns = list(set(cols).difference(selected_columns))
        df_excluded = self.df_create_entityList.drop(columns=exclude_columns)
        df = df_excluded[selected_columns]

        df.rename(columns={label: 'label'}, inplace=True)
        headers = list(df)

        for header in headers:
            self.countUnderScores = 0
            if header in self.underScoreColumns:
                newHeader = header.replace('_', '-')
                df.rename(columns={header: newHeader}, inplace=True)
                self.countUnderScores += 1

        self.entity_properties = list(df)
        deduplicate = ["label", "__id", "base_version"]

        for dd in deduplicate:
            try:
                self.entity_properties.remove(dd)
            except Exception:
                pass

        if self.dlg.onlycsv.isChecked():
            df.rename(columns={'__id': 'name'}, inplace=True)
            self.onlycsv = True
        else:
            self.onlycsv = False

        # Check if base_version exists and drop if all values = 0
        if (len(df['base_version'].drop_duplicates()) == 1 and
            df.loc[0, 'base_version'] == 0):
            df = df.drop('base_version', axis=1)

        self.updateEntityTable(df)
        self.df_export_entities = df
        self.dlg.export_Elist_to_csv.setEnabled(True)
        self.check_list_exists()
        self.Check_ColumnName(self.df_export_entities)

        if self.underScoreMessage:
            self.set_msg(self.underScoreMessage, "background: none", 'entities')

    def entity_to_CSV(self):
        """Export entity list to CSV file."""
        self.entityCsvFilename, fileType = QFileDialog.getSaveFileName(
            None,
            'Select filename for ' + str(self.dlg.Elist_name.text()) + ' entity list',
            str(self.dlg.Elist_name.text()),
            "*.csv"
        )

        if self.entityCsvFilename:
            if self.dlg.onlycsv.isChecked() and self.onlycsv is False:
                self.df_export_entities.rename(columns={'eid': 'name'}, inplace=True)
                self.onlycsv = True

            if self.onlycsv is False:
                self.dlg.radio_new_code.show()
                self.dlg.radio_append_code.show()
                self.dlg.radio_update_code.show()
                self.dlg.radio_delete_code.show()
                self.dlg.label_radio_group.show()
                self.pyodk_calls()
                self.dlg.pyODK_code.setPlainText(self.pyodk_recipe)

            self.reset_msg()

            try:
                self.df_export_entities.to_csv(str(self.entityCsvFilename), index=False)
                self.set_msg(self.tr_csv_exported + str(self.entityCsvFilename),
                             "background: none", 'entities')
            except Exception:
                self.set_msg(self.tr_file_error, "background: red", 'entities')
        else:
            self.set_msg(self.tr_select_filepath, "background: red", 'entities')

    def updateEntityTable(self, df_table):
        """Display entity preview table."""
        self.dlg.pyODK_code.resize(0, 0)
        self.dlg.Entity_table.resize(801, 215)
        self.dlg.Entity_table.clear()

        if df_table.shape[0] == 0:
            return
        else:
            headings = list(df_table)

            self.dlg.Entity_table.setRowCount(df_table.shape[0])
            self.dlg.Entity_table.setColumnCount(df_table.shape[1])
            self.dlg.Entity_table.setHorizontalHeaderLabels(headings)
            self.dlg.Entity_table.resizeColumnsToContents()

            df_array = df_table.values
            for row in range(df_table.shape[0]):
                for col in range(df_table.shape[1]):
                    self.dlg.Entity_table.setItem(
                        row, col, QTableWidgetItem(str(df_array[row, col]))
                    )

    def pyodk_calls(self):
        """Generate pyODK script for entity operations."""
        if not self.entityCsvFilename:
            self.set_msg(self.tr_select_csv, "background: none", 'entities')

        if self.dlg.export_Elist_to_csv.isEnabled():
            self.dlg.Entity_table.resize(0, 0)
            self.dlg.pyODK_code.resize(801, 215)

            entity_props = ",".join(self.entity_properties)
            entity_props = entity_props.replace(',', '", "')

            if not self.selected_projectID:
                self.selected_projectID_entity = '0 # Please manually change to the relevant Project ID integer'
            else:
                self.selected_projectID_entity = str(self.selected_projectID)

            # Generate pyODK script header
            self.pyodk_recipe = (
                f'# This pyODK script was exported from QuODK to {self.list_type} entities\n\n'
                '# NOTE: [Boring warning alert!] You remain responsible for checking:\n'
                '#    a) that the code is correct\n'
                '#    b) you understand the complexities of managing entities - it is not possible to anticipate every scenario or workflow with QuODK recipes - the more complex your situation the more difficult it is to cope with your requirements using these methods (offline entities, multiple forms updating the entities etc)\n'
                '#    c) that you are prepared if things go wrong (have you made a back up / snapshot of your Central installation on the server before starting?) \n\n'
            )

            if self.underScoreMessage:
                self.pyodk_recipe += (
                    f'#### CAUTION: {self.underScoreMessage}\n'
                    ' Please check if any entity_properties need to be updated from the list below and you may need to edit the csv file to change the column headings.\n'
                )

            self.pyodk_recipe += (
                'from pyodk.client import Client\n'
                'from csv import DictReader\n'
                'from pathlib import Path\n'
                '#Variables from QuODK\n'
                f'projectId = {self.selected_projectID_entity}\n'
                f'listname="{self.dlg.Elist_name.text()}"\n'
                f'csv_path = "{self.entityCsvFilename}"\n'
                'entity_label_field = "label"\n'
                f'entity_properties = ("{entity_props}")\n'
                'eid = "__id" # This is the uuid field for the entity\n\n\n'
                'with Client(project_id=projectId) as client, open(csv_path) as csv_file:\n'
            )

            if self.list_type == "update":
                if self.dlg.Elist_name.text() == self.dlg.dataset_existing.currentText():
                    e_props = ' '.join(self.e_prop)
                    self.pyodk_recipe += f'# The entity list on Central has the following properties: {e_props} (check they are the same as the list you have saved)\n'
                else:
                    self.pyodk_recipe += '# Caution: you have not selected an existing list - please check that the properties match those in your csv\n'

                self.pyodk_recipe += (
                    '    version = "base_version"\n'
                    '#(adapted from https://forum.getodk.org/t/updating-entities-with-csv-upload/48986)\n'
                    '    for row in DictReader(csv_file):\n'
                    '        client.entities.update(\n'
                    '                row[eid],\n'
                    '                entity_list_name = listname,\n'
                    '                label = row[entity_label_field],\n'
                    '                data={k: str(v) for k, v in row.items() if k in entity_properties},\n'
                    '                base_version=int(row[version])\n'
                    '                #force = True #uncomment this if you want to update regardless of potential conflicts - toggle with base_version\n'
                    '                )\n'
                    '        print (\'Updating entity id \',row[eid])\n'
                    '    print (\'Complete\')'
                )
                return self.pyodk_recipe

            if self.list_type == "delete":
                self.pyodk_recipe += (
                    '    for row in DictReader(csv_file):\n'
                    '        client.entities.delete(\n'
                    '                uuid = row[eid],\n'
                    '                entity_list_name = listname,\n'
                    '                )\n\n'
                    '        print (\'Deleting entity id \',row[eid])\n'
                    '    print (\'Complete\') '
                )

                if self.dlg.QGIS_KEY.currentIndex() == 0:
                    self.set_msg(self.tr_no_uuid, "background: none", 'entities')
                    self.pyodk_recipe += '#CAUTION: the Entity ids in the CSV file do not match the Central server - this script will not find any Entities to delete'

                return self.pyodk_recipe

            if self.list_type == "duplicate":
                self.pyodk_recipe += (
                    f'        #CAUTION: You have chosen an Entity List name that duplicates (or is upper/lower case equivalent) to an existing List within this project with id {self.selected_projectID_entity}. This script may not work! The rest of this script assumes creating a new list but you may need to change the projectId or listname.'
                )
                self.list_type = "create"

            if self.list_type == "create":
                self.pyodk_recipe += (
                    '        # Create a new entity list first. If you are appending to an existing list choose the append script\n'
                    '    entity_list = client.entity_lists.create(entity_list_name=listname)\n'
                    '    for prop in entity_properties:\n'
                    '        client.entity_lists.add_property(name=prop, entity_list_name=listname)\n\n'
                    '        print (\'Creating entity property: \',prop)\n'
                )

            if self.list_type == "append":
                self.pyodk_recipe += '#NOTE: when appending, make sure that the properties match those in Central to avoid failure.\n\n'

            if self.list_type == "create" or self.list_type == "append":
                self.pyodk_recipe += (
                    '    for row in DictReader(csv_file):\n'
                    '        client.entities.create(\n'
                    '            label=row[entity_label_field],\n'
                    '            uuid = row[eid], #remove or comment out this line to allow ODK Central to create the uuid (useful if you have already tested the csv and created entities with these uuids!)\n'
                    '            data={k: str(v) for k, v in row.items() if k in entity_properties},\n'
                    '            entity_list_name=listname,\n'
                    '            )\n\n'
                    '        print (\'Uploading entity id: \',row[eid])\n'
                    '    print (\'Complete\') '
                )
        else:
            self.pyodk_recipe = ""

        return self.pyodk_recipe

    def pyodk_buttons(self):
        """Handle radio button selection for pyODK operation type."""
        if self.pyodk_radio.checkedId() == 1:
            self.list_type = "create"
        elif self.pyodk_radio.checkedId() == 2:
            self.list_type = "append"
        elif self.pyodk_radio.checkedId() == 3:
            self.list_type = "update"
        elif self.pyodk_radio.checkedId() == 4:
            self.list_type = "delete"
        elif self.pyodk_radio.checkedId() == 5:
            self.list_type = "merge"

        self.pyodk_calls()
        self.dlg.pyODK_code.setPlainText(self.pyodk_recipe)