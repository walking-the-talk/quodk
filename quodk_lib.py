class SessionToken:
    """
    Manages ODK Central session authentication tokens.
    Handles login and token caching to avoid repeated authentication.
    """

    def __init__(self, odk_url, username, password):
        """
        Initialize session token manager.

        Args:
            odk_url: ODK Central server URL
            username: Email address for authentication
            password: Password for authentication
        """
        self.odk_url = odk_url.rstrip('/')  # Remove trailing slash
        self.username = username
        self.password = password
        self.token = ''

    def get_token(self):
        """
        Get session token, logging in if necessary.

        Returns:
            str: Session token on success

        Raises:
            ConnectionError: If unable to connect to server
            ValueError: If credentials are invalid
        """
        # Return cached token if valid
        if len(self.token) > 5:
            return self.token

        # Authenticate with ODK Central
        session_url = f"{self.odk_url}/v1/sessions"

        try:
            response = requests.post(
                session_url,
                data=json.dumps({
                    "email": self.username,
                    "password": self.password
                }),
                headers={"Content-Type": "application/json"},
                timeout=REQUEST_TIMEOUT
            )

            if response.status_code == 200:
                self.token = response.json()["token"]
                return self.token
            else:
                print (f"Authentication failed: {response.status_code}")
                raise ValueError(f"Authentication failed: {response.status_code}")

        except Timeout:
            print(f"Request timed out connecting to {self.odk_url}")
            raise ConnectionError(f"Request timed out connecting to {self.odk_url}")

        except RequestException as e:
            print(f"Request failed: {str(e)}")
            raise ConnectionError(f"Request failed: {str(e)}")


