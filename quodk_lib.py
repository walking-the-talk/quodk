class CentralDataGrab:
    """
    Main class for retrieving data from ODK Central via API.
    Handles projects, forms, submissions, entities, and repeat groups.
    """

    def __init__(self, session_token, odk_url):
        """
        Initialize data accessor.

        Args:
            session_token: Valid ODK Central session token
            odk_url: ODK Central server URL
        """
        self.session_token = session_token
        self.odk_url = odk_url.rstrip('/')
        self.projects_url = f"{self.odk_url}/v1/projects/"

    def _make_request(self, method, url, **kwargs):
        """
        Internal method to make HTTP requests with standardized timeout and error handling.
        """
        if 'timeout' not in kwargs:
            kwargs['timeout'] = REQUEST_TIMEOUT

        try:
            return requests.request(method, url, **kwargs)
        except Timeout:
            print(f"Request timed out connecting to {url}")
            raise ConnectionError(f"Request timed out connecting to {url}")
        except RequestException as e:
            print(f"Request failed for {url}: {str(e)}")
            raise ConnectionError(f"Request failed: {str(e)}")

    def list_projects(self):
        """
        List all projects accessible with current credentials.

        Returns:
            list: List of project dictionaries with 'id' and 'name' keys

        Raises:
            requests.HTTPError: If request fails
        """
        response = self._make_request(
            'GET',
            self.projects_url,
            headers={"Authorization": f"Bearer {self.session_token}"}
        )
        response.raise_for_status()
        return response.json()

    def list_forms(self, project_id):
        """
        List all forms in a project.

        Args:
            project_id: Project ID number

        Returns:
            list: List of form dictionaries with 'xmlFormId' and 'name' keys
        """
        form_url = f"{self.projects_url}{project_id}/forms/"
        response = self._make_request(
            'GET',
            form_url,
            headers={"Authorization": f"Bearer {self.session_token}"}
        )
        response.raise_for_status()
        return response.json()

    def list_entity_lists(self, project_id):
        """
        List all entity lists (datasets) in a project.

        Args:
            project_id: Project ID number

        Returns:
            list: List of entity list names
        """
        dataset_url = f"{self.projects_url}{project_id}/datasets/"
        response = self._make_request(
            'GET',
            dataset_url,
            headers={"Authorization": f"Bearer {self.session_token}"}
        )

        if response.status_code == 200:
            return [ds["name"] for ds in response.json()]
        else:
            return []

    def list_repeat_groups(self, project_id, xml_form_id):
        """
        List all repeat groups within a form.

        Args:
            project_id: Project ID number
            xml_form_id: Form XML ID

        Returns:
            tuple: (repeat_data list, related_entity_list name or None)
        """
        form_url = f"{self.projects_url}{project_id}/forms/"
        svc_url = f"{form_url}{xml_form_id}.svc"
        dataset_diff_url = f"{form_url}{xml_form_id}/dataset-diff"

        # Get repeat group structure
        svc_response = self._make_request(
            'GET',
            svc_url,
            headers={"Authorization": f"Bearer {self.session_token}"}
        )

        repeat_data = []
        if svc_response.status_code == 200:
            repeat_data = svc_response.json()['value']
        else:
            repeat_data = None

        # Check for associated entity list
        related_entity_list = None
        dataset_diff_response = self._make_request(
            'GET',
            dataset_diff_url,
            headers={"Authorization": f"Bearer {self.session_token}"}
        )

        if dataset_diff_response.status_code == 200:
            try:
                dataset_diff = dataset_diff_response.json()[0]
                if dataset_diff:
                    related_entity_list = dataset_diff['name']
            except (IndexError, KeyError):
                pass

        return repeat_data, related_entity_list

    def get_submissions(self, project_id, xml_form_id, repeat_group=None, date_start=None, date_end=None,
                        top_n=None, selected_fields=None):
        """
        Get form submissions, optionally filtered by date range and repeat group.

        Args:
            project_id: Project ID number
            xml_form_id: Form XML ID
            date_start: Start date string in ODK format (optional)
            date_end: End date string in ODK format (optional)
            repeat_group: Repeat group name (optional, None for main form)
            top_n: Limit to top N results (optional)

        Returns:
            tuple: DataFrame of submissions, list of geometry options, server response code, response message (in case of error)
        """
        form_url = f"{self.projects_url}{project_id}/forms/"
        # Build filter URL
        if repeat_group != "Main": #i.e. this is a repeat group
            base_url = f"{form_url}{xml_form_id}.svc/Submissions.{repeat_group}"
            key_level = "PARENT_KEY"

            if date_start and date_end:
                filter_url = (f"{base_url}?$filter=$root/Submissions/"
                            f"__system/submissionDate ge {date_start} and "
                            f"$root/Submissions/__system/submissionDate le {date_end}")
            else:
                filter_url = base_url
        else: #i.e. this is the main form
            base_url = f"{form_url}{xml_form_id}.svc/Submissions"
            key_level = "KEY"

            if top_n: #only used when using config screen
                filter_url = f"{base_url}?$top={top_n}"
            elif selected_fields:
                filter_url = (f"{base_url}?$select={selected_fields}")
            elif date_start and date_end:
                filter_url = (f"{base_url}?$filter="
                            f"__system/submissionDate ge {date_start} and "
                            f"__system/submissionDate le {date_end}")
            else: #this will download all submissions for this form
                filter_url = base_url

        # Fetch data
        df_odk, geom_options, response_code, server_response = self._get_odata_response(filter_url, key_level, repeat_group or "Main")
        return df_odk, geom_options, response_code, server_response

