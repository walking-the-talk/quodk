def timeaware(base_time):
    """
    Convert datetime to timezone-aware format handling UTC/local time differences.
    Works across platforms including Windows without additional dependencies.

    Args:
        base_time: QDateTime or datetime object

    Returns:
        datetime: Timezone-adjusted datetime
    """
    try:
        utc_zone = ZoneInfo('UTC')
        local_zone = ZoneInfo('localtime')
        # Convert the UTC time to local time
        utc_time = base_time.replace(tzinfo=utc_zone)
        local_time = utc_time.astimezone(local_zone)
        time_diff = utc_time - local_time
    except Exception:  # Fallback for Windows/systems without ZoneInfo
        utc_time = datetime.now(timezone.utc)
        local_time = datetime.now().astimezone()
        time_diff = utc_time - local_time
    if isinstance(base_time,datetime):
        base_diff = base_time - time_diff
    else:
        base_diff = base_time.toPyDateTime() - time_diff
    return base_diff


def construct_geometry(df_layer, layerGeom, geomName, geomCol):
    """
    Convert ODK geometry coordinates to WKT format (POINTZM/LINESTRINGZM/POLYGONZM).

    Args:
        df_layer: pandas DataFrame with ODK data
        layerGeom: Geometry type (Point, LineString, Polygon, or NOGEOMETRY)
        geomName: Name of geometry field
        geomCol: Column name containing coordinates

    Returns:
        pandas DataFrame with 'geom_from_ODK' column populated with WKT strings
    """
    nogeom = 0
    #print('df_layer:',df_layer)
    for i, iData in df_layer.iterrows():
        iValues = df_layer.loc[i, geomCol]

        if iValues is None:
            # No geometry data
            WKT = "NOGEOMETRY"
            nogeom += 1
        elif type(iValues) is list:
            # Convert to table only
            if str(layerGeom) == "NOGEOMETRY":
                WKT = "NOGEOMETRY"
                nogeom += 1

            # Convert to Point WKT
            elif str(layerGeom) == "Point":
                m = str(df_layer.loc[i, str(geomName+'-accuracy')])
                XYZ = " ".join(map(str, iValues))
                WKT = ("POINTZM (" + XYZ + " " + m +")")

            # Convert to LineString WKT
            elif str(layerGeom) == "LineString":
                c = ""
                for a in iValues:
                    b = ("".join(str(a))[1:][:-1]).replace(",", "")
                    if len(a) == 2:
                        c = c + ", " + b + " 0 0"
                    elif len(a) == 3:
                        c = c + ", " + b + " 0"
                    else:
                        c = c + ", " + b
                XYZ = c[1:]
                WKT = ("LINESTRINGZM (" + XYZ +")")

            # Convert to Polygon WKT
            elif str(layerGeom) == "Polygon":
                c = ""
                for a in iValues[0]:
                    b = ("".join(str(a))[1:][:-1]).replace(",", "")
                    if len(a) == 2:
                        c = c + ", " + b + " 0 0"
                    elif len(a) == 3:
                        c = c + ", " + b + " 0"
                    else:
                        c = c + ", " + b
                XYZ = c[1:]
                WKT = ("POLYGONZM ((" + XYZ + "))")
            else:
                WKT = "NOGEOMETRY"
                nogeom += 1
        else:
            WKT = "NOGEOMETRY"
            nogeom += 1

        # Add geometry to dataframe
        df_layer.loc[i, 'geom_from_ODK'] = str(WKT)

    return df_layer, nogeom


def construct_entity_geometry(df_layer, geomCol):
    """
    Convert entity geometry (stored as semicolon-separated coordinates) to WKT format.
    Generates separate layers for each geometry type if entities have mixed geometries.

    Args:
        df_layer: pandas DataFrame with entity data
        geomCol: Column name containing geometry string

    Returns:
        tuple: (DataFrame with 'geom_from_ODK' column, string of geometry types found)
    """
    geomTypes = ''

    for i, iData in df_layer.iterrows():
        iValues = df_layer.loc[i, geomCol]

        if iValues:
            # Parse semicolon-separated coordinates: "lat lon alt acc; lat lon alt acc; ..."
            vertices = iValues.split(";")
            c = ""

            # Determine geometry type based on vertex count and closure
            if len(vertices) == 1:
                geomType = "POINT"
            elif vertices[0] == vertices[-1].lstrip(' '):
                geomType = "POLYGON"
            else:
                geomType = "LINESTRING"

            # Convert each vertex from "lat lon alt acc" to "lon lat alt acc" (x y z m)
            for a in vertices:
                a = a.lstrip(' ')  # Remove leading spaces
                coords = a.split(" ")
                # Swap lat/lon to lon/lat for WKT format
                b = coords[1] + " " + coords[0] + " " + coords[2] + " " + coords[3]
                c = c + ", " + b

            XYZ = c[1:]
            WKT = (geomType + "ZM ((" + XYZ +"))")
            df_layer.loc[i, 'geom_from_ODK'] = str(WKT)
        else:
            geomType = "NOGEOMETRY"
            df_layer.loc[i, 'geom_from_ODK'] = 'NOGEOMETRY'

        # Track unique geometry types found
        if geomType not in geomTypes:
            geomTypes += str(geomType + ' ')

    return df_layer, geomTypes.strip()


def check_column_underscore(df_table, underscore_columns):
    """
    Check for underscore-containing column names that may have been transformed by OData.
    OData substitutes dash (-) for underscore (_) which can cause issues.

    Args:
        df_table: pandas DataFrame
        underscore_columns: List of expected underscore column names

    Returns:
        list: Column names containing underscores (excluding system columns)
    """
    headings = list(df_table)
    underscored = []

    for heading in headings:
        # Check for single underscore (not double underscore system columns, not base_version)
        if '_' in heading and '__' not in heading and 'base_version' not in heading:
            underscored.append(heading)

    return underscored

def load_layer_to_canvas(df_layer, LayerName, keyLevel,geomType, projection='EPSG:4326'):
    nogeom = 0
    calc = geomType.find('_calculated_geometry_')
    if keyLevel == "ENTITY_KEY" or calc != -1:
        # Entity geometry
        geomName = str(geomType)
        geomCol = str(geomType)
        # Construct entity geometry (or combined geometry)
        df_canvas_layer, entityGeom = construct_entity_geometry(df_layer, geomCol)
        layerGeom = entityGeom
    else:
        if "NOGEOMETRY" in geomType:
            layerGeom = "NOGEOMETRY"
            geomName = ""
            geomCol = "geom_from_ODK"
        else:
            # Submission geometry
            layerGeom = str(str(geomType).split("--->")[-1:][0])
            geomName = str(str(geomType).split("--->")[0])
            geomCol = f"{geomName}-coordinates"
        df_canvas_layer, nogeom = construct_geometry(df_layer, layerGeom, geomName, geomCol)
    # Create QGIS layer for each geometry type
    for geoms in layerGeom.split():

        df_splitlayer = df_canvas_layer[df_canvas_layer['geom_from_ODK'].str.contains(geoms.upper())]
        df_splitlayer.index = range(len(df_splitlayer.index))
        if geoms == "NOGEOMETRY":
            LayerType = f"POINTZM?crs={projection}"
            layer_name = f"{LayerName}_TABLE"
        else:

            LayerType = f"{geoms.upper()}ZM?crs={projection}"
            layer_name = f"{LayerName}_{geoms.upper()}"
        # print(self.projection, layer_name, LayerType)
        # Create memory layer
        temp = QgsVectorLayer(LayerType, layer_name, "memory")
        temp_data = temp.dataProvider()
        temp.startEditing()

        # Create fields
        headers = list(df_splitlayer)
        ignoregeom = ["geom_from_ODK", "entity_geom", "geometry", "calculated_geometry"]

        for header in headers:
            if header not in ignoregeom:
                datatype = df_splitlayer[header].dtype

                if datatype == 'object':
                    temp_data.addAttributes([QgsField(header, QMetaType.Type.QString)])
                elif datatype == 'int64':
                    temp_data.addAttributes([QgsField(header, QMetaType.Type.Int)])
                elif datatype == 'float64':
                    temp_data.addAttributes([QgsField(header, QMetaType.Type.Double, 'double', 20, 3)])
                else:
                    temp_data.addAttributes([QgsField(header, QMetaType.Type.QString)])

        temp.updateFields()

        # Add features with geometry
        for i, row in df_splitlayer.iterrows():
            if str(geoms) == "NOGEOMETRY":
                geom = ""
            else:
                geom = df_splitlayer.loc[i, 'geom_from_ODK']

            qFeat = QgsFeature()
            base_geom = QgsGeometry.fromWkt(geom)

            # Transform to target CRS
            sourceCrs = QgsCoordinateReferenceSystem("EPSG:4326")
            destCrs = QgsCoordinateReferenceSystem(projection)
            tr = QgsCoordinateTransform(sourceCrs, destCrs, QgsProject.instance())

            if str(geoms) != "NOGEOMETRY":
                base_geom.transform(tr)

            qFeat.setGeometry(base_geom)
            temp_data.addFeatures([qFeat])
            temp.commitChanges()
            temp.updateFields()
            temp.updateExtents()

        # Populate attribute table
        features = temp.getFeatures()
        count = 0

        for feature in features:
            for header in headers:
                if header not in ignoregeom:
                    with edit(temp):
                        datatype = df_splitlayer[header].dtype
                        attributeValue = df_splitlayer.loc[count, header]

                        if attributeValue is None:
                            pass
                        elif datatype == 'object':
                            feature[header] = str(attributeValue)
                        else:
                            if isnan(attributeValue):
                                feature[header] = None
                            else:
                                feature[header] = float(attributeValue)

                        temp.updateFeature(feature)
            count += 1

        temp.commitChanges()
        QgsProject.instance().addMapLayer(temp)
        return nogeom, count

